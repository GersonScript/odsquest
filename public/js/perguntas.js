class Perguntas {
  constructor(quizData) {
    this.quizData = quizData;
  }

  obterPerguntasPorOds(ods) {
    const odsEncontrado = this.quizData.find((item) => item.ods === ods);
    return odsEncontrado ? odsEncontrado.perguntas : [];
  }

  sortearPergunta(ods) {
    const perguntas = this.obterPerguntasPorOds(ods);
    if (perguntas.length === 0) {
      return null;
    }
    const indiceAleatorio = Math.floor(Math.random() * perguntas.length);
    return perguntas[indiceAleatorio];
  }
}
export default Perguntas;
